#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	len;

	if (!s && !*s)
		return (ft_strdup(""));
	len = ft_strlen(s);
	while (len >= 0)
	{
		if (s[len] == (char)c)
			return (&((char *)s)[len]);
		len--;
	}
	if (c == 0)
		return (&((char *)s)[len]);
	return (0);
}
